package com.team.pojo;

public class Proposi {
    private int id;
    private String proName;
    private String content;
    private String score;
    private String time;
    private String proRequ;
    private String othRequ;
    private int state;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getProName() {
        return proName;
    }

    public void setProName(String proName) {
        this.proName = proName;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getProRequ() {
        return proRequ;
    }

    public void setProRequ(String proRequ) {
        this.proRequ = proRequ;
    }

    public String getOthRequ() {
        return othRequ;
    }

    public void setOthRequ(String othRequ) {
        this.othRequ = othRequ;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }
}
